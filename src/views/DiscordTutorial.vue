<h1>How to find your Discord ID</h1>
<P>Locating your Discord ID might not be as intuitive as you think since it's hidden by default and requires turning on an important setting. The steps below explain how to locate and copy your 18-digit identification code.</P>
<h2>Finding your Discord ID on the desktop web browser</h2>
<ol>
    <li> Open the <a href="https://discord.com/" rel="noopener noreferrer" target="_blank">Discord home page</a> on your web browser. </li>
    <li> Click on <strong>Open Discord</strong> and log in to your account.</li>
    <li> Locate and click the gear icon by your Discord tag to access your settings. <br>
        <img src="https://static0.anpoimages.com/wordpress/wp-content/uploads/2022/07/DiscordID-web-browser2.JPG">
    </li>
    <li> In your settings, scroll the left side of your screen and select <strong>Advanced</strong> under <strong>App Settings</strong>. <br>
        <img src="https://static0.anpoimages.com/wordpress/wp-content/uploads/2022/07/DiscordID-web-browser3.JPG">
    </li>
    <li> Turn on <strong>Developer Mode</strong> using the toggle button. <br>
        <img src="https://static0.anpoimages.com/wordpress/wp-content/uploads/2022/07/DiscordID-web-browser4.JPG">
    </li>
    <li> Return to your settings, select <strong>My Account</strong>, click on the horizontal dots next to your Discord tag, then click <strong>Copy ID</strong>. <br>
        <img src="https://static1.anpoimages.com/wordpresshttps://static0.anpoimages.com/wordpress/wp-content/uploads/2022/07/DiscordID-web-browser5.JPG">
    </li>
</ol>
